bool Trad2Simp::func(char* pcDest, const char* pcSrc,
		const unsigned int dwSrcLen) {
	if (NULL == pcSrc || NULL == pcDest || dwSrcLen == 0)
		return false;

	unsigned int dwOrignalCount = 0;
	unsigned int dwTargetCount = 0;
	unsigned short uwChineseCode = 0;
	unsigned char* pChieseCode = (unsigned char*) &uwChineseCode;
	while (dwOrignalCount < dwSrcLen) {
		if (pcSrc[dwOrignalCount] < 0) {
			uwChineseCode =
					m_trad2simp_tbl[(unsigned char) pcSrc[dwOrignalCount]][(unsigned char) pcSrc[dwOrignalCount
							+ 1]];
			pcDest[dwTargetCount] = pChieseCode[0];
			pcDest[dwTargetCount + 1] = pChieseCode[1];
			dwOrignalCount += 2;
			dwTargetCount += 2;
		} else {
			pcDest[dwTargetCount++] = pcSrc[dwOrignalCount++];
		}
	}

	return true;
}

string Trad2Simp::func(const string &str_src) {
	string str_dest;
	if (str_src.size() < 128) {
		char pcDest[128];
		func(pcDest, str_src.c_str(), str_src.size());
		pcDest[str_src.size()] = 0;
		str_dest.assign(pcDest, str_src.size());
	}else {
		str_dest = str_src;
	}

	return str_dest;
}
